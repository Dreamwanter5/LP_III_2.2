public class Equipamento {
    private String nomeEquipamento;
    private int bonusAtaque, bonusDefesa, bonusVida, bonusAgilidade, bonusMana;

    public String getNomeEquipamento() {
        return nomeEquipamento;
    }

    public void setNomeEquipamento(String nomeEquipamento) {
        this.nomeEquipamento = nomeEquipamento;
    }

    public int getBonusAtaque() {
        return bonusAtaque;
    }

    public void setBonusAtaque(int bonusAtaque) {
        this.bonusAtaque = bonusAtaque;
    }

    public int getBonusDefesa() {
        return bonusDefesa;
    }

    public void setBonusDefesa(int bonusDefesa) {
        this.bonusDefesa = bonusDefesa;
    }

    public int getBonusVida() {
        return bonusVida;
    }

    public void setBonusVida(int bonusVida) {
        this.bonusVida = bonusVida;
    }

    public int getBonusAgilidade() {
        return bonusAgilidade;
    }

    public void setBonusAgilidade(int bonusAgilidade) {
        this.bonusAgilidade = bonusAgilidade;
    }

    public int getBonusMana() {
        return bonusMana;
    }

    public void setBonusMana(int bonusMana) {
        this.bonusMana = bonusMana;
    }
    
}
